public class AssertJClassForTest {
    private int age;
    private String name;
    private int year;

    public AssertJClassForTest(int age, String name, int year) {
        this.age = age;
        this.name = name;
        this.year = year;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }
}
